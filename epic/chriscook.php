	<h2>Persona</h2>
	<p>Chris Cook is an assistant coach for a Division II College baseball team. In the off-season part of his job is to recruit high school players to come be a student athlete at his College. This entails long days on the road in addition to trying to find out which players he needs to scout to be able to then recruit. Part of this is scouring the internet to find schedules and locations of games. He wants a place where he can find all of these things in one place</p>
